SELECT 
	ALUNO,
	NOME_COMPL, TURMA, SB.SIT_MATRICULA, 
	SB.GRUPO, LANC_DEB, BANCO, AGENCIA, CONTA_BANCO, CONVENIO,	ITEMCOBRANCA, 
	COBRANCA, BOLETO, VALOR_PAGAR,
	DATA_DE_VENCIMENTO, VOUCHER, DESC_PERC_VALOR, DESCONTO
FROM 
	VW_FCAV_PGTOS_SEM_BOLETOS SB
	INNER JOIN LY_AGREGA_ITEM_COBRANCA IC
		ON IC.GRUPO = SB.GRUPO
WHERE SIT_ALUNO = 'ATIVO'
--AND ALUNO IN ('A201900313', 'A201900263', 'A201900301')
GROUP BY ALUNO,	NOME_COMPL, TURMA, SB.GRUPO, SB.SIT_MATRICULA, 
		 LANC_DEB, BANCO, AGENCIA, CONTA_BANCO, ITEMCOBRANCA,
		 CONVENIO, COBRANCA, BOLETO, VALOR_PAGAR, 
		 DATA_DE_VENCIMENTO,VOUCHER, DESC_PERC_VALOR, DESCONTO
ORDER BY DATA_DE_VENCIMENTO, TURMA, ALUNO



