SELECT DISTINCT A.NUM_FUNC  FROM   LY_AGENDA A,   LY_MATRICULA M  WHERE   A.ANO = M.ANO   AND A.SEMESTRE = M.SEMESTRE  AND A.DISCIPLINA = M.DISCIPLINA   AND A.TURMA = 'CEAI T 28'    AND M.SIT_MATRICULA = 'Matriculado'   AND NUM_FUNC IN (900043,83484) GROUP BY A.NUM_FUNC

SELECT A.DISCIPLINA FROM LY_AGENDA A,   LY_MATRICULA M  WHERE   A.ANO = M.ANO AND A.SEMESTRE = M.SEMESTRE AND A.DISCIPLINA = M.DISCIPLINA AND A.TURMA = 'CEAI T 28' AND M.SIT_MATRICULA = 'Matriculado' AND A.DISCIPLINA IN ('CEAI-CF','CEAI-EB') GROUP BY A.DISCIPLINA

SELECT  A.NUM_FUNC  FROM   LY_AGENDA A,   LY_MATRICULA M  WHERE   A.ANO = M.ANO       AND A.SEMESTRE = M.SEMESTRE       AND A.DISCIPLINA = M.DISCIPLINA      AND A.TURMA = 'CCGB T 42'  AND M.SIT_MATRICULA = 'Matriculado' AND NUM_FUNC IN (900001,90557)   GROUP BY A.NUM_FUNC

SELECT A.NUM_FUNC  FROM   LY_AGENDA A,   LY_MATRICULA M  WHERE   A.ANO = M.ANO       AND A.SEMESTRE = M.SEMESTRE       AND A.DISCIPLINA = M.DISCIPLINA       AND A.TURMA = 'CEGP T 65'   AND M.SIT_MATRICULA = 'Matriculado'  AND NUM_FUNC IN (900012, 900250) GROUP BY A.NUM_FUNC