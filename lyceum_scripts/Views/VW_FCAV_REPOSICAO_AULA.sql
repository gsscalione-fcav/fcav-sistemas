ALTER VIEW VW_FCAV_REPOSICAO_AULA
AS

SELECT 
	REPOSICAO,
	(SELECT DATA 
	 FROM LY_AGENDA 
	 WHERE AGENDA = A.REPOSICAO) AS REPOSICAO_DO_DIA,
AGENDA,
TURNO,
FACULDADE,
DIA_SEMANA,
AULA,
DISCIPLINA,
TURMA,
ANO,
SEMESTRE,
DEPENDENCIA,
NUM_FUNC,
DATA,
HORA_INICIO,
HORA_FIM,
DESCRICAO,
LISTA,
GERA_LISTA,
CANCELADA,
EVENTO_CUMP,
NUM_FUNC_SUBST,
MOTIVO_SUBST,
TIPO_ATIVIDADE,
OCORRENCIA,
MOTIVO,
TIPO_DOCENTE,
TIPO_TUTORIA,
SUBDISCIPLINA,
CONFIRMADA,
MINUTOS
from LY_AGENDA A
WHERE REPOSICAO IS NOT NULL
