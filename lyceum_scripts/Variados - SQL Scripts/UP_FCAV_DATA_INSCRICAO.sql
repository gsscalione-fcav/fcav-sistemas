/*
	PREENCHER A DATA DE INSCRIÇÃO DOS CANDIDATOS ABAIXO NA FCAV_CANDIDATOS
*/

USE LYCEUM
GO

SELECT 
	CANDIDATO,
	DATA_INSC 
FROM 
	FCAV_CANDIDATOS 
where
	CANDIDATO in ('201700311', '201700099')

GO
	
disable trigger [TR_FCAV_SELEC_COORD] on [FCAV_CANDIDATOS] 
go

update FCAV_CANDIDATOS
SET	DATA_INSC = '2017-03-02 00:00:00.000'
WHERE CANDIDATO = '201700311'

update FCAV_CANDIDATOS
SET	DATA_INSC = '2016-12-17 00:00:00.000'
WHERE CANDIDATO = '201700099'

GO

enable trigger [TR_FCAV_SELEC_COORD] on [FCAV_CANDIDATOS] 


SELECT 
	CANDIDATO,
	DATA_INSC 
FROM 
	FCAV_CANDIDATOS 
