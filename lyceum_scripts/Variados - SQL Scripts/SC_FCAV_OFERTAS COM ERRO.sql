SELECT 
	OFERTA_DE_CURSO,
	CURSO,
	TURNO,
	CURRICULO,
	CURSO_OFERTADO
FROM 
	LY_OFERTA_CURSO 
WHERE 
	DESCRICAO_COMPL like '%A-AAL9001.15 T 04%'
	CURSO = 'A-AAL9001.15'


UPDATE LY_OFERTA_CURSO
SET
	CURSO_OFERTADO = UPPER(OC.CURSO)
FROM 
	LY_OFERTA_CURSO OC
WHERE
	OC.ID_SUB_GRUPO_OFERTA = 8
	AND ANO_INGRESSO = 2017
	AND PER_INGRESSO = 0
	AND DTFIM >=GETDATE()
	AND CURSO_OFERTADO != OC.CURSO
	AND CURSO NOT LIKE 'ATUALIZACAO'
	 


